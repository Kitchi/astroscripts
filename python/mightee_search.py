def parse_coordinate(ra_str, dec_str):
    """Parse RA/DEC as either decimal degrees or HMS/DMS."""
    try:
        ra = float(ra_str)
        dec = float(dec_str)
        return SkyCoord(ra=ra * u.deg, dec=dec * u.deg)
    except ValueError:
        try:
            return SkyCoord(ra_str, dec_str, unit=(u.hourangle, u.deg))
        except ValueError:
            raise(f"Coordinates are not valid. Need to be in HMS/DMS or decimal degrees.")


def mightee_search(ra_str, dec_str, top=3):
    input_coord = parse_coordinate(ra_str, dec_str)
    pointing_coords = SkyCoord(
        ra=[p[2] for p in MIGHTEE_POINTINGS] * u.deg,
        dec=[p[3] for p in MIGHTEE_POINTINGS] * u.deg,
    )
    seps = input_coord.separation(pointing_coords)
    indices = seps.argsort()[:top]

    print(f"Input: RA={input_coord.ra.deg:.5f} deg, DEC={input_coord.dec.deg:.5f} deg")
    print(f"{'Capture Block':<16} {'Field':<20} {'RA':>12} {'DEC':>12} {'Sep':>12}")
    print("-" * 74)
    for i in indices:
        cb, name, ra, dec = MIGHTEE_POINTINGS[i]
        print(f"{cb:<16} {name:<20} {ra:>12.5f} {dec:>12.5f} {seps[i].to(u.arcmin):>12.4f}")


